function cardPayment(){
    document.getElementById('methods').style.display='block'
    document.getElementById('cardPayment').style.display='block'
    document.getElementById('bkashPayment').style.display='none'
    document.getElementById('cashPayment').style.display='none'
}
function bkashPayment(){
    document.getElementById('methods').style.display='block'
    document.getElementById('cardPayment').style.display='none'
    document.getElementById('bkashPayment').style.display='block'
    document.getElementById('cashPayment').style.display='none'
}
function cashPayment(){
    document.getElementById('methods').style.display='block'
    document.getElementById('cardPayment').style.display='none'
    document.getElementById('bkashPayment').style.display='none'
    document.getElementById('cashPayment').style.display='block'
}
